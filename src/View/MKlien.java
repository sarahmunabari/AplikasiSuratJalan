    private void cmdhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdhapusActionPerformed
        // TODO add your handling code here: 
        int hapus = JOptionPane.showConfirmDialog(this, "Anda Yakin Ingin Hapus Data?", "Konfirmasi",
        JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (hapus == JOptionPane.YES_OPTION) {
            try{
                kon.QUERY("DELETE FROM tb_klien WHERE id_klien ='"+txtkdklien.getText()+"'","di Hapus");
                showTabel();
                Bersih();
                txtkdklien.setEditable(true);
                txtkdklien.requestFocus();
            }catch(Exception sqle){
                JOptionPane.showMessageDialog(rootPane,"Data Gagal Simpan"+sqle.getMessage());
            }
        }
    }//GEN-LAST:event_cmdhapusActionPerformed

