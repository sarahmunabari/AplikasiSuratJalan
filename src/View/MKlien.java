    private void BuatTabel(){
        model=new DefaultTableModel();
        model.addColumn("ID Klien");
        model.addColumn("Nama Klien");
        model.addColumn("Telepon");
        model.addColumn("Alamat");
        tblklien.setModel(model);
    }
     
    public void showTabel(){
      removeTable();        
        try{
          con=kon.open();
          st=con.createStatement();
          
          query="SELECT * FROM tb_klien order by id_klien  asc";
         
          ResultSet rs=st.executeQuery(query);
          while(rs.next()){
              String a =rs.getString("id_klien");
              String b=rs.getString("nama_klien");
              String c=rs.getString("no_telp"); 
              String d=rs.getString("alamat"); 
              String data[]={a,b,c,d};          
              model.addRow(data);         
          }
      }catch(SQLException sqle){
          JOptionPane.showMessageDialog(null,"Data Gagal Masuk Tabel"+sqle);
      }  
    }
    
    public void removeTable(){
        try{for(int t=model.getRowCount();t>0;t--) {model.removeRow(0);}
        }catch(Exception ex){ System.out.println(ex);}
    }
